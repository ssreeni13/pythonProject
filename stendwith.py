# String StartsWith and EndsWith (str.startswith(prefix) and str.endswith(suffix)):
# Checking if a string starts or ends with a specific substring.

text = "Hello, world!"
starts_with_hello = text.startswith("Hello")  # True
ends_with_exclamation = text.endswith("!")  # True
print(starts_with_hello)
print(ends_with_exclamation)